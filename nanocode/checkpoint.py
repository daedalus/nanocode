def meta_dir(session_id: str, data_dir: str | None = None) -> Path:
    """Get session memory root directory.

    Houses checkpoint artifacts, task narratives, and other per-session memory files.
    """
    if data_dir is None:
        import os

        xdg_data = os.environ.get("XDG_DATA_HOME", str(Path.home() / ".local" / "share"))
        data_dir = str(Path(xdg_data) / "nanocode")
    return Path(data_dir) / "memory" / "sessions" / session_id


def checkpoint_path(session_id: str, data_dir: str | None = None) -> Path:
    """Get path to session checkpoint file."""
    return meta_dir(session_id, data_dir) / "checkpoint.md"


def memory_path(project_id: str = "default", data_dir: str | None = None) -> Path:
    """Get path to project memory file."""
    if data_dir is None:
        import os

        xdg_data = os.environ.get("XDG_DATA_HOME", str(Path.home() / ".local" / "share"))
        data_dir = str(Path(xdg_data) / "nanocode")
    return Path(data_dir) / "memory" / "projects" / project_id / "MEMORY.md"


def global_memory_path(data_dir: str | None = None) -> Path:
    """Get path to global memory file."""
    if data_dir is None:
        import os

        xdg_data = os.environ.get("XDG_DATA_HOME", str(Path.home() / ".local" / "share"))
        data_dir = str(Path(xdg_data) / "nanocode")
    return Path(data_dir) / "memory" / "global" / "MEMORY.md"


def notes_path(session_id: str, data_dir: str | None = None) -> Path:
    """Get path to session notes file."""
    return meta_dir(session_id, data_dir) / "notes.md"


def tasks_dir(session_id: str, data_dir: str | None = None) -> Path:
    """Get path to session tasks directory."""
    return meta_dir(session_id, data_dir) / "tasks"


def progress_path(session_id: str, task_id: str, data_dir: str | None = None) -> Path:
    """Get path to per-task progress journal."""
    return tasks_dir(session_id, data_dir) / task_id / "progress.md"


def ensure_checkpoint_template(session_id: str, data_dir: str | None = None) -> Path:
    """Create checkpoint file from template if it doesn't exist.

    Returns path to the checkpoint file.
    """
    path = checkpoint_path(session_id, data_dir)
    if not path.exists():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(CHECKPOINT_TEMPLATE)
    return path


def ensure_memory_template(
    project_id: str = "default", data_dir: str | None = None
) -> Path:
    """Create memory file from template if it doesn't exist.

    Returns path to the memory file.
    """
    path = memory_path(project_id, data_dir)
    if not path.exists():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(MEMORY_TEMPLATE)
    return path


def ensure_notes_template(session_id: str, data_dir: str | None = None) -> Path:
    """Create notes file from template if it doesn't exist.

    Returns path to the notes file.
    """
    path = notes_path(session_id, data_dir)
    if not path.exists():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(NOTES_TEMPLATE)
    return path
