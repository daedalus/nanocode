"""Structured checkpoint template and path helpers.

Ported from MiMo-Code's session/checkpoint-templates.ts and checkpoint-paths.ts.
Provides 11-section checkpoint format with per-section token budgets.
"""

from dataclasses import dataclass
from pathlib import Path


@dataclass
class SectionBudget:
    """Token budget for a checkpoint section."""

    name: str
    budget: int


CHECKPOINT_TEMPLATE = """# Session checkpoint
_Generated by checkpoint writer; structure preserved across updates. Edit only content under italic instructions._

## §1 Active intent
_User's most recent explicit request, verbatim block-quoted from the conversation._

(none yet)

## §2 Next concrete action
_The single next concrete step, derived from §1 and current state._

(none yet)

## §3 Directives (this session)
_This session's specific working style. Project-level rules are in MEMORY.md — do not duplicate them here._

(none)

## §4 Task tree
_Hierarchical view of tasks with current focus marked._

(none yet)

## §5 Current work
_Description of what was being done immediately before this checkpoint. Mention specific file paths._

(none yet)

## §6 Files and code sections
_Files actively being read or modified. List with one-line purpose._

(none yet)

## §7 Discovered knowledge (cross-task)
_Facts learned during this session that may apply to future tasks._

(none yet)

## §8 Errors and fixes
_Errors encountered this session and how they were resolved. Newest first._

(none)

## §9 Live resources
_Runtime state: branch, uncommitted files, running processes, temp artifacts._

(none yet)

## §10 Design decisions and discussion outcomes
_Decisions reached through discussion that produced no immediate code artifact._

(none yet)

## §11 Open notes
_Catch-all for items that don't fit §1-§10._

(none yet)
"""

MEMORY_TEMPLATE = """# Project memory
_Durable project-level knowledge. Persists across all sessions._

## Project context
_What is this project? What's its goal?_

(none yet)

## Rules
_Hard constraints that every session must respect._

(none yet)

## Architecture decisions
_Major design choices with rationale._

(none yet)

## Discovered durable knowledge
_Cross-task facts that survive across sessions._

(none yet)
"""

NOTES_TEMPLATE = """# Session notes
_Free-form scratchpad for the main agent. Append entries as you go._
"""

